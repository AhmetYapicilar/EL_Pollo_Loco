/**
 * Class representing the state of keyboard keys.
 */
class Keyboard {
  /**
   * Whether the left arrow key is pressed.
   * @type {boolean}
   */
  LEFT = false;

  /**
   * Whether the right arrow key is pressed.
   * @type {boolean}
   */
  RIGHT = false;

  /**
   * Whether the down arrow key is pressed.
   * @type {boolean}
   */
  DOWN = false;

  /**
   * Whether the up arrow key is pressed.
   * @type {boolean}
   */
  UP = false;

  /**
   * Whether the spacebar key is pressed.
   * @type {boolean}
   */
  SPACE = false;

  /**
   * Whether the 'D' key is pressed.
   * @type {boolean}
   */
  D = false;
}
